package com.example.myapplication.Sqlite.Modal;

public class SqliteModal {

    String name, email, courseName,phoneNumber,gender;
    int id ;

    public SqliteModal(String name, String email, String courseName, String phoneNumber, String gender) {
        this.name = name;
        this.email = email;
        this.courseName = courseName;
        this.phoneNumber = phoneNumber;
        this.gender = gender;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCourseName() {
        return courseName;
    }

    public void setCourseName(String courseName) {
        this.courseName = courseName;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }
}
